// Helper: generate a symmetric matrix from a flat upper-triangle array
function buildMatrix(size, values) {
  const m = Array.from({ length: size }, () => Array(size).fill(1))
  let idx = 0
  for (let i = 0; i < size; i++) {
    for (let j = i + 1; j < size; j++) {
      m[i][j] = values[idx]
      m[j][i] = values[idx]
      idx++
    }
  }
  return m
}

