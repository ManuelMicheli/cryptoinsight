export function t(key, lang) {
  const entry = translations[key]
  if (!entry) return key
  return entry[lang] || entry.it || key
}

/** Extract localized value from a bilingual object or return as-is if it's a plain string */
export function l(value, lang) {
  if (!value) return ''
  if (typeof value === 'string') return value
  return value[lang] || value.it || ''
}
